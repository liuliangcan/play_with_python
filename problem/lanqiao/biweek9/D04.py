#       ms
def solve():
    n, = RI()
    a = RILST()
    cnt = [0]*31
    ans = 0
    for v in a[::-1]:
        for i in range(31):
            if v >> i & 1:
                ans += cnt[i]
                cnt[i] += 1
    print(ans)


